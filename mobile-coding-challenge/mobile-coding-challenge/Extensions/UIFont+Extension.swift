import UIKit

extension UIFont {
    class func ProximaNovaBold(ofSize: CGFloat) -> UIFont {
        return UIFont(name: "ProximaNova-Bold", size: ofSize)!
    }
    
    class func ProximaNovaLight(ofSize: CGFloat) -> UIFont {
        return UIFont(name: "ProximaNova-Light", size: ofSize)!
    }
    
    class func ProximaNovaRegular(ofSize: CGFloat) -> UIFont {
        return UIFont(name: "ProximaNova-Regular", size: ofSize)!
    }
    
    class func ProximaNovaRegularItalic(ofSize: CGFloat) -> UIFont {
        return UIFont(name: "ProximaNova-RegularItalic", size: ofSize)!
    }
    
    class func ProximaNovaSemibold(ofSize: CGFloat) -> UIFont {
        return UIFont(name: "ProximaNova-Semibold", size: ofSize)!
    }
}
